#[async_trait]
impl OpsModule for AdvisorModule {
    fn name(&self) -> &str {
        "mod-advisor"
    }

    fn version(&self) -> &str {
        "0.1.0"
    }

    fn description(&self) -> &str {
        "Intelligent Ops Advisor — rule-based operational suggestions and best practice checks"
    }

    fn dependencies(&self) -> Vec<&str> {
        vec![]
    }

    fn tools(&self) -> Vec<ToolDefinition> {
        vec![
            ToolDefinition {
                name: "advisor_list".into(),
                description: "List operational suggestions with optional filters".into(),
                parameters: serde_json::json!({
                    "type": "object",
                    "properties": {
                        "severity": { "type": "string", "description": "Filter by severity: info, warning, critical" },
                        "category": { "type": "string", "description": "Filter by category: security, performance, reliability, best_practice" },
                        "limit": { "type": "integer", "description": "Max suggestions to return (default 20)", "default": 20 }
                    }
                }),
            },
            ToolDefinition {
                name: "advisor_acknowledge".into(),
                description: "Mark a suggestion as acknowledged".into(),
                parameters: serde_json::json!({
                    "type": "object",
                    "properties": {
                        "id": { "type": "string", "description": "Suggestion ID" }
                    },
                    "required": ["id"]
                }),
            },
            ToolDefinition {
                name: "advisor_dismiss".into(),
                description: "Dismiss (soft-delete) a suggestion".into(),
                parameters: serde_json::json!({
                    "type": "object",
                    "properties": {
                        "id": { "type": "string", "description": "Suggestion ID" }
                    },
                    "required": ["id"]
                }),
            },
            ToolDefinition {
                name: "advisor_stats".into(),
                description: "Get aggregate statistics of suggestions".into(),
                parameters: serde_json::json!({
                    "type": "object",
                    "properties": {}
                }),
            },
            ToolDefinition {
                name: "advisor_health_report".into(),
                description: "Generate an operational health report summary".into(),
                parameters: serde_json::json!({
                    "type": "object",
                    "properties": {}
                }),
            },
        ]
    }

    async fn execute(
        &self,
        _ctx: &ModuleContext,
        tool: &str,
        params: serde_json::Value,
    ) -> anyhow::Result<serde_json::Value> {
        match tool {
            "advisor_list" => {
                let severity = params["severity"].as_str();
                let category = params["category"].as_str();
                let limit = params["limit"].as_u64().unwrap_or(20) as u32;

                let suggestions = self.list_suggestions(severity, category, limit).await?;
                Ok(serde_json::json!({ "suggestions": suggestions }))
            }
            "advisor_acknowledge" => {
                let id = params["id"]
                    .as_str()
                    .ok_or_else(|| anyhow::anyhow!("missing 'id' parameter"))?;

                let acked = self.acknowledge_suggestion(id).await?;
                Ok(serde_json::json!({ "acknowledged": acked, "id": id }))
            }
            "advisor_dismiss" => {
                let id = params["id"]
                    .as_str()
                    .ok_or_else(|| anyhow::anyhow!("missing 'id' parameter"))?;

                let dismissed = self.dismiss_suggestion(id).await?;
                Ok(serde_json::json!({ "dismissed": dismissed, "id": id }))
            }
            "advisor_stats" => {
                let stats = self.stats().await?;
                Ok(stats)
            }
            "advisor_health_report" => {
                let report = self.health_report().await?;
                Ok(report)
            }
            _ => Err(anyhow::anyhow!("unknown tool: {}", tool)),
        }
    }

    async fn on_event(&self, _ctx: &ModuleContext, event: &OpsEvent) -> Option<ModuleAction> {
        let _suggestions = self.evaluate_and_store(event).await.ok()?;
        None
    }

    async fn health_check(&self, _ctx: &ModuleContext) -> HealthStatus {
        HealthStatus::Healthy
    }
}

